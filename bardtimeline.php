			<h2>Bard Timeline</h2>
			<div>
				<p>
					<strong>1853</strong> - John Bard and his bride Margaret Johnston Bard buy Blithewood, Robert Donaldson's estate along the Hudson River, which they rename Annandale. It's to become the site of St.Stephen's College and later Bard College.
				</p>
				<p>
					<strong><a href="http://www.bard.edu/archives/images/timeline/chapel.jpg"><img src="http://www.bard.edu/archives/images/timeline/chapel-sm.jpg" alt="chapel" width="127" height="100" hspace="2" vspace="4" border="0" align="right"></a>1859</strong> - Present Day chapel is built, a gift of Mr. And Mrs. John Bard.
				</p>
				<p>
					<strong>1860 - </strong>St. Stephen's College is chartered by the legislature of the State of New York. The first board of Trustees is established and the Reverend George Frederick Seymour is appointed Warden. Six students take up residence (there were sixty applicants for admission) and begin their classical training in preparation for the seminaries of the Episcopal Church.
				</p>
				<p>
					<strong>1866 - </strong>The college's charter is amended to authorize the granting of degrees in the liberal arts and sciences, in addition to the pre-seminarian program.
				</p>
				<p>
					<strong>1919</strong> - Dr. Bernard Iddings Bell, educator and theologian, arrives as seventh president of St. Stephen's College. 
				</p>
				<p>
					<strong><a href="http://www.bard.edu/archives/images/timeline/gym.jpg"><img src="http://www.bard.edu/archives/images/timeline/gym-sm.jpg" alt="gym" width="154" height="100" hspace="2" vspace="4" border="0" align="left" style="padding-right: 10px;"></a>1920 - </strong>Cornerstone is laid for the new Memorial Gymnasium. A Sociology department is established by Dr. Lyford P. Edwards.
				</p>
				<p>
					<strong>1928</strong> - St. Stephen's becomes an undergraduate college of Columbia University.
				</p>
				<p>
					<strong>1929</strong> - Franklin Delano Roosevelt becomes a trustee and serves until 1933.
				</p>
				<p>
					<strong>1933-1944</strong> - A number of distinguished European emigres join the Bard faculty. Among them are Felix Hirsch, the political editor; Emil Hauser, the violinist; Adolf Sturmthal, the economist; and Werner Wolff, the psychologist. Stefan Hirsch, precisionist painter; also joins Bard at this time. 
				</p>
				<p>
					<strong>1934</strong> - The college's name is changed to Bard College. Donald Tewksbury, who became dean in 1933, publishes An Educational Program for Bard College. Based on the Oxford tutorial, this was a revolutionary progressive program for undergraduate education in this country. 
				</p>
				<p>
					<strong>1944</strong> - Bard withdraws from affiliation with Columbia in order to become coeducational.The first women students arrive in September. The college has been considering coeducation for at least 5 years. F. W. Dupee, and Franco Modigliani join Bard faculty.
				</p>
				<p>
					<strong><a href="http://www.bard.edu/archives/images/timeline/wxbc.jpg"><img src="http://www.bard.edu/archives/images/timeline/wxbc-sm.jpg" alt="wxbc" width="119" height="100" hspace="4" vspace="2" border="0" align="right"></a>1946 - </strong>College enrollment reaches an all time high of 301. Mary McCarthy joins Bard faculty.
				</p>
				<p>
					<strong>1947</strong> - <a href="http://wxbc.bard.edu/">WXBC</a> the college run radio station is started as a senior project
				</p>
				<p>
					<strong><a href="http://www.bard.edu/archives/images/timeline/blithewood.jpg"><img src="http://www.bard.edu/archives/images/timeline/blithewood-sm.jpg" alt="Blithewood" width="143" height="100" hspace="4" vspace="2" border="0" align="left" style="padding-right: 10px;"></a>1951</strong> - The 825-acre Zabriskie estate, including Blithewood mansion, is given to the college.
				</p>
				<p>
					<strong>1952</strong> - The Common Course, designed by <a href="http://www.bard.edu/bluecher/">Heinrich Bl&uuml;cher</a> and forerunner of the <a href="https://fys.bard.edu/">First-Year Seminar</a>, is inaugurated.
				</p>
				<p>
					<strong>1960</strong> - The college celebrates its centennial year. Bard's thirteenth president, Reamer Kline, takes office for a fourteen-year tenure, during which the college undergoes a tremendous expansion in buildings, grounds, faculty and student body size, as well as core curricula.
				</p>
				<p>
					<strong>1971</strong> - Kline Commons is built as a new student center. Bard College Field Station is built.
				</p>
				<p>
					<strong><a href="http://www.bard.edu/archives/images/timeline/leon.jpg"><img src="http://www.bard.edu/archives/images/timeline/leon-sm.jpg" alt="Leon sitting in chair" width="77" height="100" hspace="4" vspace="2" border="0" align="right"></a>1975</strong> - Leon Botstein becomes Bard's fourteenth president, inaugurating a period of growth in the range and distinction of the faculty and of visiting scholars. Botstein expands the college's program, combining the progressive tutorial system with the classical legacy of St. Stephen's.
				</p>
				<p>
					<strong>1979</strong> - Bard assumes responsibility for <a href="http://www.simons-rock.edu/">Simon's Rock Early College</a> in Great Barrington, Massachusetts.
				</p>
				<p>
					<strong>1981</strong> - Edith C. Blum Art Institute is built as part of the <a href="http://www.bard.edu/mfa/">Milton and Sally Avery Graduate Schoo</a>l, a program offering a master of fine arts degree. The Institute for Writing and Thinking is founded, and the first workshop in <a href="https://languageandthinking.bard.edu/">Language and Thinking</a> for entering students is held.
				</p>
				<p>
					<strong>1982</strong> - The Institute for Writing and Thinking was established through grants from Exxon following the second year of Bard's Language and Thinking workshop. The purpose of the Institute was to offer professional development in writing to secondary and college teachers of all subjects, taking the best practices from &quot;L&amp;T&quot; and adapting them to the needs of secondary and college teachers everywhere. President Botstein appointed Paul Connolly (a member of<br> the Language and Thinking Faculty) as Director and Teresa Vilardi as
				associate director.
			</p>
				<p>
					<strong>1986</strong> - The <a href="http://www.levy.org/">Jerome Levy Economics Institute</a> of Bard College is founded (now the Levy Economics Institute.)
				</p>
				<p>
					<strong>1989</strong> - Extensive campus construction and renovation is undertaken. The Franklin W. Olin Humanities building is completed. The David Rose Science Laboratories, the Stevenson Gymnasium and swimming pool, and a new cluster of residences hall known as Alumni Houses are built.
				</p>
				<p>
					<strong>1990</strong> - The <a href="http://ccs.bard.edu/">Center for Curatorial Studies</a> is founded. The Program for International Education (PIE) is founded to bring young people from emerging democracies to study at Bard for a year examining issues of democratic transition. The <a href="http://www.bard.edu/bmf/2008/">Bard Music Festival</a> begins its first season.
				</p>
				<p>
					<strong>1991</strong> - Undergraduate enrollment reaches 1,000.
				</p>
				<p>
					<strong>1992 - </strong>The Center for Curatorial Studies in Art in Contemporary Culture opens, housing the Rivendell Collection of Late Twentieth-Century Arts.
				</p>
				<p>
					<strong><a href="http://www.bard.edu/archives/images/timeline/venturi.jpg"><img src="http://www.bard.edu/archives/images/timeline/venturi-sm.jpg" alt="venturi drawing" width="154" height="100" hspace="4" vspace="2" border="0" align="left" style="padding-right: 10px;"></a>1993 - </strong>The <a href="https://www.bgc.bard.edu/">Bard Graduate Center for Studies in the Decorative Arts</a> opens in New York City. The new <a href="http://www.bard.edu/library/">Charles P. Stevenson Jr. Library</a>, designed by Robert Venturi, opens.
				</p>
				<p>
					<strong>1998</strong> - The <a href="http://www.bard.edu/iile/">Institute for International Liberal Education</a> is founded. Heinz O. and Elizabeth C. Bertelsmann campus center opens; it houses meeting rooms, the post office, The Down the Road Caf&eacute; and the bookstore, and the Weis Cinema.
				</p>
				<p>
					<strong>2001 - </strong>Bard and the Board of Education in New York City collaborate in creating <a href="http://www.bard.edu/bhsec/">Bard High School Early College (BHSEC)</a>, an alternative to traditional high school. <a href="https://bpi.bard.edu/">Bard Prison Initiative</a> is established.
				</p>
				<p>
					<strong><a href="http://www.bard.edu/archives/images/timeline/fisher.jpg"><img src="http://www.bard.edu/archives/images/timeline/fisher-sm.jpg" alt="fisher" width="153" height="100" hspace="4" vspace="2" border="0" align="right"></a>2002</strong> - <a href="http://www.bard.edu/hrp/">Human Rights</a> program established, the first full academic concentration in humans rights at a US college. 
				</p>
				<p>
					<strong>2003 - </strong>The college celebrates the opening of the <a href="http://fishercenter.bard.edu/">Richard B. Fisher Center for Performing Arts</a> designed by Frank Gehry.
				</p>
				<p>
					<strong>2004</strong> - The <a href="http://www.bard.edu/mat/">Masters of Arts in Teaching Program</a> enrolls its first class.
				</p>
				<p>
					<strong>2005 - </strong>Groundbreaking for new <a href="http://www.bard.edu/scienceinitiative/">Science Center</a> expected to open in Fall 2007. <a href="http://www.bard.edu/conservatory/">Bard College Conservatory of Music</a> is opened, a five year, dual bachelor degree program.
				</p>
				<p>
					<strong>2006</strong> - Bard College Conservatory of Music establishes graduate programs in two fields, offering Master of Music degrees in vocal arts and conducting (<a href="https://www.bard.edu/conservatory/gcp/">Conductor's Institute</a> at Bard is now affiliated with the Conservatory). Center for Curatorial Studies inaugurates the 17,000 square foot <a href="http://ccs.bard.edu/museum/">Hessel Museum of Art</a> designed by Goettsch Partners architects. 
				</p>
